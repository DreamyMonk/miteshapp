String _hex(Color c) =>
    '#${c.value.toRadixString(16).padLeft(8, '0').substring(2)}';

/// Renders inline SVG inner-markup (paths/circles/etc.) exactly like the
/// prototype's inline `<svg viewBox="0 0 24 24">…</svg>` icons.
class Ico extends StatelessWidget {
  final String inner;
  final double size;
  final Color stroke;
  final Color? fill;
  final double sw;
  final bool round;
  const Ico(
    this.inner, {
    super.key,
    this.size = 18,
    this.stroke = Colors.white,
    this.fill,
    this.sw = 1.8,
    this.round = true,
  });

  @override
  Widget build(BuildContext context) {
    final caps = round ? ' stroke-linecap="round" stroke-linejoin="round"' : '';
    final f = fill == null ? 'none' : _hex(fill!);
    final s =
        '<svg viewBox="0 0 24 24" fill="$f" stroke="${_hex(stroke)}" stroke-width="$sw"$caps>$inner</svg>';
    return SvgPicture.string(s, width: size, height: size);
  }
}

